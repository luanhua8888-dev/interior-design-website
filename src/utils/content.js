export function getContent(key) {
  const customContent = localStorage.getItem('aura_custom_content');
  if (customContent) {
    try {
      const parsed = JSON.parse(customContent);
      if (parsed[key] !== undefined) {
        return parsed[key];
      }
    } catch (e) {
      console.error('Error parsing custom content', e);
    }
  }
  return defaultContent[key];
}

export function saveContent(key, value) {
  const customContent = localStorage.getItem('aura_custom_content') || '{}';
  try {
    const parsed = JSON.parse(customContent);
    parsed[key] = value;
    localStorage.setItem('aura_custom_content', JSON.stringify(parsed));
  } catch (e) {
    console.error('Error saving custom content', e);
  }
}

export function getAllContent() {
  const customContent = localStorage.getItem('aura_custom_content');
  let parsed = {};
  if (customContent) {
    try {
      parsed = JSON.parse(customContent);
    } catch (e) {
      console.error(e);
    }
  }
  
  const result = {};
  Object.keys(defaultContent).forEach(key => {
    result[key] = parsed[key] !== undefined ? parsed[key] : defaultContent[key];
  });
  return result;
}

export function resetAllContent() {
  localStorage.removeItem('aura_custom_content');
}
